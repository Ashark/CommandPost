--- === plugins.core.menu.helpandsupport ===
---
--- The 'Help & Support' menu section.

--------------------------------------------------------------------------------
--
-- CONSTANTS:
--
--------------------------------------------------------------------------------
local PRIORITY = 8888888

--------------------------------------------------------------------------------
--
-- THE PLUGIN:
--
--------------------------------------------------------------------------------
local plugin = {
	id				= "core.menu.helpandsupport",
	group			= "core",
	dependencies	= {
		["core.menu.bottom"] = "bottom",
	}
}

	--------------------------------------------------------------------------------
	-- INITIALISE PLUGIN:
	--------------------------------------------------------------------------------
	function plugin.init(dependencies)
		local section = dependencies.bottom:addSection(PRIORITY)
			--:addSeparator(100)
			:addMenu(0, function() return i18n("helpAndSupport") end)

		return section
	end

return plugin